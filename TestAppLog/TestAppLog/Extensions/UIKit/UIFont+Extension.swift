import UIKit

extension UIFont {
    static func montserratBold26() -> UIFont? {
        return UIFont.init(name: "Montserrat-Bold", size: 26)
    }
    
    static func montserratRegular14() -> UIFont? {
        return UIFont.init(name: "Montserrat-Regular", size: 14)
    }
    
    static func montserratRegular20() -> UIFont? {
        return UIFont.init(name: "Montserrat-Regular", size: 20)
    }
    
    
}
